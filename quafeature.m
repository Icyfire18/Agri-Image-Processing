function x=quafeature(img1,img2)
[m n]=size(img1);
a=double(img1);
b=double(img2);
c=a^2;
d=b^2;
e=(a-b)^2;
MSE=sum(sum((e)/(m*n)));
PSR= 10*log(max(max((c)/ MSE)));
SNR=log(sum(sum((c)/(n*m*MSE))));
SC=sum(sum((c)/(d)));
MD=max(mad(a-b));
AD=sum(sum((a-b)/(n*m)));
NAE=(mad(a-b))/(mad(a));
NXC=sum(sum((a*b)/c));
ed1=edge(img1,'sobel');
ed2=edge(img2,'sobel');
Ted=mad(ed1-ed2);
TED=sum(Ted);
C1=corner(img1);
D1=round(mean(C1));
CD1=D1(1);
C2=corner(img2);
D2=round(mean(C2));
CD2=D2(1);
TCD=(CD1-CD2)/(max(CD1,CD2));
SSI=ssim(img1,img2);
x=[MSE PSR SNR SC MD AD NAE NXC TED TCD SSI];
